package intro;

public class SuskaiciuotiNuoIki {

    public static void main(String[] args) {


        int number;
        int suma = 0;


        for (number = 1; number <= 100; number++) {

            suma = suma + number;
        }

        System.out.print("Suma nuo 1 iki 100 yra:"+ suma);

    }
}
